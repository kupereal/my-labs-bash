#!/usr/bin/env bash


# --- 1. Функция приветствия ---
print_hello()
{
    echo "Hello"
}
print_hello


# --- 2. Функция расчета формулы: (x*2+y/5-3)*11+8 ---

# Объявляем математические инструменты
add() { return `expr $1 + $2`; }    # Сложение
sub() { return `expr $1 - $2`; }    # Вычитание
mul() { return `expr $1 \* $2`; }   # Умножение
div() { return `expr $1 / $2`; }    # Деление

# Запрашиваем данные для расчета
echo -n "x="
read x
echo -n "y="
read y

# Запускаем пошаговое вычисление
mul $x 2                            # Шаг 1: x * 2
a=$?                                # Сохраняем результат x * 2
div $y 5                            # Шаг 2: y / 5
b=$?                                # Сохраняем результат y / 5
add $a $b                           # Шаг 3: складываем результаты (a + b)
sub $? 3                            # Шаг 4: вычитаем 3
mul $? 11                           # Шаг 5: умножаем на 11
add $? 8                            # Шаг 6: прибавляем 8
echo "result=$?"                    # Вывод результата
