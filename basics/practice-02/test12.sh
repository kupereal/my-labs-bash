#!/usr/bin/env bash

# --- Блок 1. Сравнение двух чисел (if / elif / else) ---
if [ $1 -eq $2 ]; then
    echo "$1 = $2"
elif [ $1 -gt $2 ]; then
    echo "$1 > $2"
else
    echo "$1 < $2"
fi

# --- Блок 2. Проверка деления нацело ---
if (( $1 % $2 == 0 )); then
    echo "Yes (делится без остатка)"   # Сработает, если остаток равен 0
else
    echo "No (есть остаток)"            # Сработает, если остаток присутствует
fi

# --- Блок 3. Выбор варианта (case) ---
echo -n "Enter number from 1 to 6: "
read n

case $n in
    1)       echo "N=1" ;;              # Точное совпадение с единицей
    2|3)     echo "N=2 or 3" ;;         # Знак | означает логическое ИЛИ
    4|5|6)   echo "N=4 or 5 or 6" ;;    # Выбор из нескольких цифр сразу
    *)       echo "Unknown" ;;          # Звездочка означает все остальное
esac
